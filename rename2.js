function operator(nodes) {
  const args = $arguments;
  const addFlag = args.flag !== false;        // 默认 true
  const prefixFirst = args.nf !== false;      // 默认 true
  const airportName = args.name ? decodeURI(args.name) : '';
  const separator = args.fgf !== undefined ? decodeURI(args.fgf) : ' ';
  
  // 输入输出类型映射
  const inType = { cn:'zh', zh:'zh', us:'en', en:'en', flag:'flag', gq:'flag', quan:'quan' }[args.in] || '';
  const outType = { cn:'zh', zh:'zh', us:'en', en:'en', flag:'flag', gq:'flag', quan:'quan' }[args.out] || 'zh';
  
  // 构建输入数组和输出数组
  let inputArray, outputArray;
  switch (inType) {
    case 'en': inputArray = EN; break;
    case 'flag': inputArray = FLAG; break;
    case 'quan': inputArray = QUAN; break;
    default: inputArray = ZH; break;
  }
  switch (outType) {
    case 'en': outputArray = EN; break;
    case 'flag': outputArray = FLAG; break;
    case 'quan': outputArray = QUAN; break;
    default: outputArray = ZH; break;
  }
  
  // 构建映射：输入词 -> 输出词
  const map = {};
  inputArray.forEach((word, idx) => {
    map[word] = outputArray[idx];
  });
  
  // 处理每个节点
  for (const node of nodes) {
    let originalName = node.name;
    let matchedRegion = null;
    // 按输入数组顺序查找第一个匹配的地区词
    for (const word of inputArray) {
      if (originalName.includes(word)) {
        matchedRegion = word;
        break;
      }
    }
    if (!matchedRegion) {
      // 未匹配到地区，若提供了机场名称则仅加前缀（不加国旗）
      if (airportName) {
        node.name = prefixFirst ? `${airportName}${separator}${originalName}` : `${originalName}${separator}${airportName}`;
      }
      continue;
    }
    
    const regionOutput = map[matchedRegion];
    let flag = '';
    if (addFlag) {
      const flagIndex = outputArray.indexOf(regionOutput);
      if (flagIndex !== -1) {
        flag = FLAG[flagIndex];
        if (flag === '🇹🇼') flag = '🇨🇳';
      }
    }
    
    // 组装最终名称：顺序为 [前缀(若前置)] + [国旗] + [地区] + [前缀(若后置)]
    let parts = [];
    if (prefixFirst && airportName) parts.push(airportName);
    if (flag) parts.push(flag);
    parts.push(regionOutput);
    if (!prefixFirst && airportName) parts.push(airportName);
    
    node.name = parts.join(separator);
  }
  return nodes;
}
